    Hace un año dimos un paso muy importante: nos constituimos legalmente como fundación. 
    Esto nos permitió organizar mejor los procesos, ampliar nuestra capacidad de ayuda y 
    crear programas formales de rescate, adopción, alimentación y apadrinamiento.
